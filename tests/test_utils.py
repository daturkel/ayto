import numpy as np

from ayto.utils import faster_permutations


def test_faster_permutations():
    assert (
        faster_permutations(4)
        == np.array(
            [
                [0, 1, 2, 3],
                [1, 0, 2, 3],
                [0, 2, 1, 3],
                [1, 2, 0, 3],
                [2, 0, 1, 3],
                [2, 1, 0, 3],
                [0, 1, 3, 2],
                [1, 0, 3, 2],
                [0, 2, 3, 1],
                [1, 2, 3, 0],
                [2, 0, 3, 1],
                [2, 1, 3, 0],
                [0, 3, 1, 2],
                [1, 3, 0, 2],
                [0, 3, 2, 1],
                [1, 3, 2, 0],
                [2, 3, 0, 1],
                [2, 3, 1, 0],
                [3, 0, 1, 2],
                [3, 1, 0, 2],
                [3, 0, 2, 1],
                [3, 1, 2, 0],
                [3, 2, 0, 1],
                [3, 2, 1, 0],
            ]
        )
    ).all()
